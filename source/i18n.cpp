namespace i18n {

    void
    set_lang(Lang lang)
        noexcept
    {
        g_lang = lang;
    }


    Lang
    current_lang()
        noexcept
    {
        return g_lang;
    }


    void
    set_lang_from_int(int v)
        noexcept
    {
        g_lang = (v != 0) ? Lang::ZH : Lang::EN;
    }


    const char *
    tr(Key key)
        noexcept
    {
        const auto idx = static_cast<std::size_t>(key);
        if (idx >= static_cast<std::size_t>(Key::Count)) {
            return "?";
        }
        return (g_lang == Lang::ZH) ? k_table[idx].zh : k_table[idx].en;
    }


    const char *
    language_name(Lang lang)
        noexcept
    {
        return (lang == Lang::ZH) ? k_table[static_cast<std::size_t>(Key::LangChinese)].zh
                                  : k_table[static_cast<std::size_t>(Key::LangEnglish)].en;
    }

} // namespace i18n
